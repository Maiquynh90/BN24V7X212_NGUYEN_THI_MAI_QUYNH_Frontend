<template>
    <div class="page"> 
        
        <div class="row mb-3"> 
            <div class="col-12">
                <InputSearch 
                    v-model="searchText" 
                    @submit="searchContacts" 
                />
            </div>
        </div>

        <div class="row">
            
            <div class="col-md-5"> 
                <h4>
                    Danh bạ
                    <i class="fas fa-address-book"></i>
                </h4>
                <ContactList
                    v-if="filteredContactsCount > 0"
                    :contacts="filteredContacts"
                    v-model:activeIndex="activeIndex"
                />
                <p v-else>Không có liên hệ nào.</p>
                
                <div class="mt-3 row justify-content-around align-items-center">
                    <button class="btn btn-sm btn-primary" @click="refreshList()">
                        <i class="fas fa-redo"></i> Làm mới
                    </button>
                    <button
                        class="btn btn-sm btn-success"
                        @click="goToAddContact"
                    >
                        <i class="fas fa-plus"></i> Thêm mới
                    </button>
                    <button
                        class="btn btn-sm btn-danger"
                        @click="removeAllContacts"
                    >
                        <i class="fas fa-trash"></i> Xóa tất cả
                    </button>
                </div>
            </div>

            <div class="col-md-7">
                <div v-if="activeContact">
                    <h4>
                        Chi tiết Liên hệ
                        <i class="fas fa-address-card"></i>
                    </h4>
                    <ContactCard :contact="activeContact" />
                    <router-link
                        :to="{
                            name: 'contact.edit',
                            params: { id: activeContact._id },
                        }"
                    >
                        <span class="btn btn-sm btn-warning mt-2">
                            <i class="fas fa-edit"></i> Hiệu chỉnh
                        </span>
                    </router-link>
                </div>
            </div>
        </div>
    </div>
</template>

<script>
import ContactCard from "@/components/ContactCard.vue"; 
import InputSearch from "@/components/InputSearch.vue"; 
import ContactList from "@/components/ContactList.vue";
import ContactService from "@/services/contact.service";

export default {
    components: {
        ContactCard, 
        InputSearch, 
        ContactList,
    },
    data() {
        return {
            contacts: [],
            activeIndex: -1,
            searchText: "",
        };
    },
    watch: {
        searchText() {
            this.activeIndex = -1;
        },
    },
    computed: {
        contactStrings() {
            return this.contacts.map((contact) => {
                const { name, email, address, phone } = contact;
                return [name, email, address, phone].join("");
            });
        },
        // ĐÃ SỬA: Thêm .toLowerCase() để bỏ phân biệt chữ hoa/thường
        filteredContacts() {
            if (!this.searchText) return this.contacts;
            const lowerCaseSearchText = this.searchText.toLowerCase();
            
            return this.contacts.filter((_contact, index) =>
                this.contactStrings[index].toLowerCase().includes(lowerCaseSearchText)
            );
        },
        activeContact() {
            if (this.activeIndex < 0) return null;
            return this.filteredContacts[this.activeIndex];
        },
        filteredContactsCount() {
            return this.filteredContacts.length;
        },
    },
    methods: {
        searchContacts() {
            this.activeIndex = -1;
        },
        async retrieveContacts() {
            try {
                this.contacts = await ContactService.getAll();
            } catch (error) {
                console.log(error);
            }
        },
        refreshList() {
            this.retrieveContacts();
            this.activeIndex = -1;
        },
        async removeAllContacts() {
            if (confirm("Bạn có chắc chắn muốn xóa tất cả Liên hệ?")) {
                try {
                    await ContactService.deleteAll();
                    this.refreshList();
                } catch (error) {
                    console.log(error);
                }
            }
        },
        goToAddContact() {
            this.$router.push({ name: "contact.add" });
        },
    },
    mounted() {
        this.refreshList();
    },
};
</script>

